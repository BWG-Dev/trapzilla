function get_closest_min_key($keys, $value) {
    // Sort keys in ascending order
    sort($keys);
    $closest_key = null;

    foreach ($keys as $key) {
        if ($key <= $value) {
            $closest_key = $key;
        } else {
            break;
        }
    }

    if($keys[0] > $value){
        return $keys[0];
    }

    return $closest_key;
}

function get_product_details($sizing_matrix, $ground_type, $size, $gpm, $grease_volume) {
    // Validate ground type and size
    if (!isset($sizing_matrix[$ground_type][$size])) {
        return "Invalid ground type or size.";
    }

    $size_data = $sizing_matrix[$ground_type][$size];

    // Find the closest GPM
    $grease_keys = array_keys($size_data);
    $closest_grease_volume = get_closest_min_key($grease_keys, $grease_volume);

    if ($closest_grease_volume === null) {
        return "No matching grease volume found.";
    }

    // Find the closest grease volume
    $gpm_keys = array_keys($size_data[$closest_grease_volume]);
    $closest_gpm = get_closest_min_key($gpm_keys, $gpm);

    if ($closest_gpm === null) {
        return "No matching gpm found.";
    }


    // Return the SKUs and quantities
    return $size_data[$closest_grease_volume][$closest_gpm];
}

